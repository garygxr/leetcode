func canPermutePalindrome(s string) bool {
	var hashMap = make(map[byte]int)
	for _, i2 := range s {
		hashMap[byte(i2)]++
	}
	count := 0
	for b := range hashMap {
		i := hashMap[b]
		if i%2 != 0 {
			count++
		}
	}
	return count <= 1
}

